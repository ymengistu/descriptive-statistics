
# paste & paste0 Functions in R to Concatenate Strings  +++++++++++++

paste0("I", "ride", "my", "bike", 25, "times")
paste("I", "ride", "my", "bike", 25, "times")
paste("I", "ride", "my", "bike", 25, "times", sep="_")


